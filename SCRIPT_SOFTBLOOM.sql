CREATE VIEW VProducto AS
    SELECT ROW_NUMBER()
        OVER(
            ORDER BY
                pr.idpro DESC
        ) AS fila,
        pr.idpro,
        pr.nompro,
		pr.prepro,
       pr.despro,
        pr.tippro,
		pr.stockpro,
        pr.estpro,
        concat(concat (p.razsocprov,' '), p.rucprov) AS PROVEEDOR
      FROM PRODUCTO pr
        INNER JOIN PROVEEDOR p ON
        pr.idprov = p.idprov;    
        
